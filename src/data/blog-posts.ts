// Helper function to get a post by handle
export function getBlogPostByHandle(handle: string): BlogPost | undefined {
  return blogPosts.find(post => post.handle === handle);
}

// Helper function to get all posts (for static generation)
export function getAllBlogPosts(): BlogPost[] {
  return blogPosts;
}

// Helper function to get related posts (by category or tags)
export function getRelatedPosts(currentHandle: string, limit: number = 3): BlogPost[] {
  const currentPost = getBlogPostByHandle(currentHandle);
  if (!currentPost) return [];

  return blogPosts
    .filter(post => post.handle !== currentHandle)
    .filter(post => 
      post.category === currentPost.category || 
      post.tags.some(tag => currentPost.tags.includes(tag))
    )
    .slice(0, limit);
}